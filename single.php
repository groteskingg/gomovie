							<div class="entry-content">
								<p>“Cruella” is a vaguely retro costume party with a doggedly retro playlist — a treat for fashion-curious kids whipped up by the boomers and Gen Xers who hold the keys to the Disney I.P. storage locker. And there’s a millennial Oscar winner in the titular role. When I say it has something for everyone I’m not being sarcastic, though I’m also not being entirely complimentary.</p>
								<p>This revisionist supervillain origin story, directed by Craig Gillespie (“I, Tonya”), doesn’t offer much that is genuinely new, but it nonetheless feels fresher than most recent Disney live-action efforts. There’s some visual wit and pop sparkle in the mildly Dickensian tale of how Cruella DeVil, the notorious pooch-hater of “One Hundred and One Dalmatians,” came to be that way.</p>
								<p>Reviewing the original animated adaptation of Dodie Smith’s novel for The Times in 1961, Howard Thompson observed that “the kids who survived ‘Psycho’ should survive Cruella.” Pretty scary stuff! Times change: No puppies, C.G.I. or otherwise, are harmed in this movie. Cruella — originally known as Estella and played by a harmlessly snarly Emma Stone — actually likes dogs (though she does have a specific grudge against Dalmatians).</p>
								<p>This is not “Joker,” so Cruella’s transgressive energies are kept within the bounds of social acceptability and the PG-13 rating. Her motive is revenge, and her methods include fraud, theft and deceit, but the closest she comes to evil is occasional selfish insensitivity to her friends. She isn’t a monster. She’s an artist, and her theatrically outrageous misbehavior is a sign of her uncompromising creativity.</p>
								<p>Cruella’s swaggering, eclectic spirit aligns with the film’s idea of London in the 1970s, its alleged setting. The aesthetic is raffish, glammish and also punkish, and the musical selections zigzag through the years from “Their Satanic Majesties Request” to “London Calling.” No deep cuts here, just an eclectic sampling of Dad Rock Essentials. The choices can be a little on the nose — Stone’s first appearance as the grown-up Estella, with hair dyed crimson, is heralded by “She’s a Rainbow” — but my middle-aged ears were not offended. Special kudos to Gillespie and Susan Jacobs, the music supervisor, for including the Stooges’ “I Wanna Be Your Dog,” a song with no kinky subtext whatsoever and as such a perfect fit for a “Dalmatians” spinoff.</p>
								<p>The oldies greatest-hits package, linked by Nicholas Britell’s elegant score, keeps things lively even when the plot turns draggy or hectic. Jenny Beavan’s costumes and Fiona Crombie’s production design, festooning posh department stores, bohemian thrift shops and couture palaces, engage the eye even when the characters wander through the city in search of coherent motives.<p> 
								<p>Estella starts out as a renegade schoolgirl (played by Tipper Seifert-Cleveland) with two-tone hair, and soon lands in London, orphaned and alone. She befriends a pair of pickpockets, Jasper and Horace, who grow up to be Joel Fry and Paul Walter Hauser, providing cartoon-sidekick japery as the horizon of Estella’s ambition shifts from petty crime to high fashion. At that point, she adds a journalist (Kirby Howell-Baptiste) and a used-clothing aficionado (John McCrea) to her retinue. <p> 
								<p>Estella’s nemesis and role model is a famous designer known as the Baroness, a self-described genius who recalls Meryl Streep in “The Devil Wears Prada,” Daniel Day-Lewis in “Phantom Thread” and, of course, Cruella DeVil in both her cartoon and Glenn Close incarnations. Luckily, the role belongs to Emma Thompson, who plays her as a haughty, feline predator alternately annoyed, enraged and charmed by Stone’s angry mouse. <p> 
								<p>The film itself traffics in less intense emotions, which makes it easy enough to watch but hard to care much about. Its main purpose is to remind you that other movies exist, which might describe Disney’s current business strategy as a whole. At best, it might also inspire you to spin some old records or play dress up with those weird clothes that have languished in the back of the closet through these grim athleisure months.<p> 
